function prevReview() {
    if (currentReviewIndex<=0) {
        currentReviewIndex = reviews.length - 1;
    }else {
        currentReviewIndex = currentReviewIndex - 1;
    }
    carousel ();
}

function nextReview() {
    if (currentReviewIndex>= reviews.length - 1) {
        currentReviewIndex = reviews.length - reviews.length;
    }else {
        currentReviewIndex = currentReviewIndex + 1;
    }
    carousel ();
}
