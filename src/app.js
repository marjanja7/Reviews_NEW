function carousel () {
    let container = document.getElementById('container');
    let review = reviews[currentReviewIndex];
    container.innerHTML = `
        <div class="carousel">
            <h2 class="title">${review.title}</h2>
            <div class="photo">
                <img class="image" src="${review.photo}"/>
            </div>
            <h4 class="name">${review.name}</h4>
            <p class="rating">Оценка: ${review.rating}</p>
            <p class="info">${review.info}</p>
            <span class="date">${review.date}</span>
         </div>  
     `
}


